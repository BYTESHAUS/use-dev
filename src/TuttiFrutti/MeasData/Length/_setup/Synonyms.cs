namespace MeasData.Length.Setup;
public abstract class Synonyms
{
    protected const string millimeter = nameof(millimeter);
    protected const string mm = millimeter;

    protected const string centimeter = nameof(centimeter);
    protected const string cm = centimeter;

    protected const string meter = nameof(meter);
    protected const string metre = meter;
    protected const string meters = meter;
    protected const string m = meter;

    protected const string kilometer = nameof(kilometer);
    protected const string kilometers = kilometer;
    protected const string km = kilometer;

    protected const string foot = nameof(foot);
    protected const string feet = foot;
    protected const string ft = foot;

    protected const string mile = nameof(mile);
    protected const string miles = mile;
    protected const string mi = mile;

    protected const string nautical_mile = "nmi";
    protected const string nautical_miles = nautical_mile;
    protected const string NM = nautical_mile;
    protected const string nmi = nautical_mile;

    protected const string yard = nameof(yard);
    protected const string yd = yard;
    protected const string yards = yard;

    protected const string inch = nameof(inch);
    protected const string @in = inch;
}
